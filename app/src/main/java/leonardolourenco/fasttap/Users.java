package leonardolourenco.fasttap;

public class Users {
    private int id;
    private String UserName;
    private int GStar;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserName() {
        return UserName;
    }

    public void setUserName(String userName) {
        UserName = userName;
    }

    public int getGStar() {
        return GStar;
    }

    public void setGStar(int GStar) {
        this.GStar = GStar;
    }
}
